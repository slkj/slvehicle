package cn.slkj.clgl.person.entity;

public class Driver_record {
	private String driver_record_id;
	private String idcard_no;
	private String carnumber;
	private String driver_info;
	private String driver_record_time;
	private String result;
	
	
	public String getDriver_record_id() {
		return driver_record_id;
	}
	public void setDriver_record_id(String driver_record_id) {
		this.driver_record_id = driver_record_id;
	}
	public String getIdcard_no() {
		return idcard_no;
	}
	public void setIdcard_no(String idcard_no) {
		this.idcard_no = idcard_no;
	}
	
	public String getCarnumber() {
		return carnumber;
	}
	public void setCarnumber(String carnumber) {
		this.carnumber = carnumber;
	}
	public String getDriver_info() {
		return driver_info;
	}
	public void setDriver_info(String driver_info) {
		this.driver_info = driver_info;
	}
	public String getDriver_record_time() {
		return driver_record_time;
	}
	public void setDriver_record_time(String driver_record_time) {
		this.driver_record_time = driver_record_time;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	
		
}
